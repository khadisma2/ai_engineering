#   Concatenation(+)    Join 2 lists into in new list
list1 = [1, 2, 3]
list2 = [4, 5, 6, 7, 8, 9]
result = list1 + list2
print(result)

#   Repetition
nums = [1,  2]
repeated = nums * 5
print(repeated)

#   Indexing
fruits = ["apple", "banana", "cherry"]
print(fruits[0])
print(fruits[-1])
print(fruits[-2])

#   Slicing
numbers = [0, 1, 2, 3, 4, 5]
print(numbers[1:4])
print(numbers[:3])
print(numbers[::2])

#   Membership (in/not in)
colors = ["red", "green", "blue"]
print("green" in colors)

